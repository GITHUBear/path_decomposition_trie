        inline uint64_t reverse_bytes(uint64_t n) {
            n = ((n >> 8) & MAGIC_MASK_4) | ((n & MAGIC_MASK_4) << 8);
            n = ((n >> 16) & MAGIC_MASK_5) | ((n & MAGIC_MASK_5) << 16);
            n = (n >> 32) | (n << 32);
            return n;
        }

        inline uint64_t reverse_bits(uint64_t n) {
            // reverse adjacent bits
            n = ((n >> 1) & MAGIC_MASK_1) | ((n & MAGIC_MASK_1) << 1);
            n = ((n >> 2) & MAGIC_MASK_2) | ((n & MAGIC_MASK_2) << 2);
            n = ((n >> 4) & MAGIC_MASK_3) | ((n & MAGIC_MASK_3) << 4);
            return reverse_bytes(n);
        }

        inline uint64_t byte_counts(uint64_t x) {
            x = (x & MAGIC_MASK_1) + ((x & (~MAGIC_MASK_1)) >> 1);
            x = (x & MAGIC_MASK_2) + ((x & (~MAGIC_MASK_2)) >> 2);
            x = (x & MAGIC_MASK_3) + ((x & (~MAGIC_MASK_3)) >> 4);
            return x;
        }

        inline uint64_t bytes_sum(uint64_t x) {
            x = (x & MAGIC_MASK_4) + ((x & (~MAGIC_MASK_4)) >> 8);
            x = (x & MAGIC_MASK_5) + ((x & (~MAGIC_MASK_5)) >> 16);
            x = (x & MAGIC_MASK_6) + (x >> 32);
            return x;
        }

        // count set-bits in `x`.
        // idea: Divide & Conquer, similar with `reserve_bits`
        inline uint64_t popcount(uint64_t x) {
            return bytes_sum(byte_counts(x));
        }

        // REQUIRE: x has only 1 bit set.
        inline uint8_t bit_position(uint64_t x) {
            assert(popcount(x) == 1);
            return debruijn64_mapping[(x * debruijn64) >> (64 - 6)];
        }

        // RETURN: true if MSB exists and set `ret` to the index of MSB
        inline bool msb(uint64_t x, unsigned long& ret) {
            if (!x) {
                return false;
            }
            // set the bit that is lower than MSB to 1
            x |= x >> 1;
            x |= x >> 2;
            x |= x >> 4;
            x |= x >> 8;
            x |= x >> 16;
            x |= x >> 32;
            // isolate the MSB
            x ^= x >> 1;
            ret = bit_position(x);
            return true;
        }

        // REQUIRE: x != 0
        inline uint8_t msb(uint64_t x)
        {
            assert(x);
            unsigned long ret = -1U;
            msb(x, ret);
            return (uint8_t)ret;
        }

        // RETURN: true if LSB exists and set `ret` to the index of LSB
        inline bool lsb(uint64_t x, unsigned long& ret) {
            if (!x) {
                return false;
            }
            ret = bit_position(x & (-x));
            return true;
        }

        // REQUIRE: x != 0
        inline uint8_t lsb(uint64_t x)
        {
            assert(x);
            unsigned long ret = -1U;
            lsb(x, ret);
            return (uint8_t)ret;
        }

        // get position of `k`-th 1-bit in `x`.
        // `k` starts from 0.
        inline uint64_t select_in_word(const uint64_t x, const uint64_t k) {
            assert(k < popcount(x));

            uint64_t byte_sums = byte_counts(x) * BYTE_UNIT;
            const uint64_t byte_ks = k * BYTE_UNIT;

            // 0x80 in each byte is a flag that indicates k >= sums.
            const uint64_t indicator = 0x80ULL * BYTE_UNIT;
            const uint64_t byte_flag =
                    ((byte_ks | indicator) - byte_sums) & indicator;

            // `k`-th 1-bit is at `byte_block_pos / 8`-th byte.
            const uint64_t byte_block_pos = popcount(byte_flag) << 3;
            const uint64_t byte_rank =
                    k - ((byte_sums << 8) >> byte_block_pos) & (uint64_t(0xFF));
            return byte_block_pos +
                select_in_byte[((x >> byte_block_pos) & (uint64_t(0xFF))) | (byte_rank << 8)];
        }
